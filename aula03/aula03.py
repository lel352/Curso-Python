#  Tudo em python é um Objeto até os tipos primitivos.
#  Python é um linguagem de tipagem dinâmica
"""
Str - String -> São textos em dentro de aspas.
"""
print('Algumas coisa')  # python le esse texto como uma String, devido as aspas.
print('123')  # Como está dentro de aspas ele entende que é uma string não número
print("texto em aspas duplas")
print('Este texto é uma \'String\' (Str).')
print("Este texto é uma 'String' (Str).")  # como eu abri com aspas duplas ele considera as simples como string
print(r'Esse texto \n teste')  # r é para não interpretar nada dentro da String, assim não quebra a linda no \n
